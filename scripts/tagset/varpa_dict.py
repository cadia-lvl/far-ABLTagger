    parser.add_argument('--input', '-i', help='path to input file to convert', required=True)

    langs = parser.add_mutually_exclusive_group(required=True)
    langs.add_argument('--icelandic', '-is', help='input file has Icelandic tags', action='store_true')
    langs.add_argument('--faroese', '-fo', help='input file has Faroese tags', action='store_true')

    args = parser.parse_args()

    converted = 0
    missing = 0

    with open(args.input) as file:
        for line in file.readlines():
            tag = re.split(r'\s', line)[1].strip()
            # print(tag)
            if args.icelandic:
                try:
                    tag = IS_FO[tag]
                    converted += 1
                except:
                    missing +=1
                # print(tag)
            if args.faroese:
                try:
                    tag = FO_IS[tag]
                    converted += 1
                except:
                    missing +=1
                # print(tag)

    print(converted)
    print(missing)

